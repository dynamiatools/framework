package tools.dynamia.domain.test;

public class SomeEntityDTO {
    private Long id;
    private Long accountId;
    private String name;
    private String company;
    private int age;
    private boolean active;
    private String otherEntity;
    private Long otherEntityId;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public Long getAccountId() {
        return accountId;
    }

    public void setAccountId(Long accountId) {
        this.accountId = accountId;
    }

    public String getOtherEntity() {
        return otherEntity;
    }

    public void setOtherEntity(String otherEntity) {
        this.otherEntity = otherEntity;
    }

    public Long getOtherEntityId() {
        return otherEntityId;
    }

    public void setOtherEntityId(Long otherEntityId) {
        this.otherEntityId = otherEntityId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getCompany() {
        return company;
    }

    public void setCompany(String company) {
        this.company = company;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public boolean isActive() {
        return active;
    }

    public void setActive(boolean active) {
        this.active = active;
    }
}
